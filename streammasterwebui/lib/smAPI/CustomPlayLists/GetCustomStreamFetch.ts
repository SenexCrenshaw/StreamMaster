import { GetCustomStream } from '@lib/smAPI/CustomPlayLists/CustomPlayListsCommands';
import { GetCustomStreamRequest } from '../smapiTypes';
import { isSkipToken } from '@lib/common/isSkipToken';
import { Logger } from '@lib/common/logger';
import { createAsyncThunk } from '@reduxjs/toolkit';


export const fetchGetCustomStream = createAsyncThunk('cache/getGetCustomStream', async (param: GetCustomStreamRequest, thunkAPI) => {
  try {
    if (isSkipToken(param))
    {
        Logger.error('Skipping GetEPGFilePreviewById');
        return undefined;
    }
    Logger.debug('Fetching GetCustomStream');
    const response = await GetCustomStream(param);
    return {param: param, value: response };
  } catch (error) {
    console.error('Failed to fetch', error);
    return thunkAPI.rejectWithValue({ error: error || 'Unknown error', value: undefined });
  }
});


